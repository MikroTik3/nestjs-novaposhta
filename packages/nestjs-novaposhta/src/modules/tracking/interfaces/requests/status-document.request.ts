/**
 * Документ для відстеження статусу відправлення.
 */
export interface StatusDocument {

    /**
     * Номер експрес-накладної (ЕН).
     */
    DocumentNumber: string

    /**
     * Номер телефону відправника або одержувача.
     *
     * Дозволяє отримати розширену інформацію
     * про відправлення.
     */
    Phone?: string
}

/**
 * Тіло запиту для отримання статусів відправлень.
 */
export interface StatusDocumentRequest {

    /**
     * Масив документів для відстеження.
     *
     * Максимальна кількість — 100.
     */
    Documents: StatusDocument[]
}